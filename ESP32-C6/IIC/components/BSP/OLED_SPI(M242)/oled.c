void oled_draw_char(uint8_t page, uint8_t col, char ch)
{
    if (ch < 32 || ch > 126) ch = ' ';
    const uint8_t *glyph = font6x8[ch - 32];
    oled_set_pos(page, col);
    oled_write_data(glyph, 6);
}

void oled_draw_string(uint8_t page, uint8_t col, const char *str)
{
    while (*str && col < 128) {
        oled_draw_char(page, col, *str);
        col += 6;
        str++;
    }
}