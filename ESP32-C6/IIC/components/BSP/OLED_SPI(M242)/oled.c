#include "oled.h"
#include "driver/gpio.h"
#include "driver/spi_master.h"
#include "esp_log.h"
#include <string.h>

static const char *TAG = "SSD1309";
static spi_device_handle_t spi;

#define OLED_CMD_MODE   0
#define OLED_DATA_MODE  1

static void oled_set_dc(int level)
{
    gpio_set_level(OLED_DC_GPIO, level);
}

static void oled_set_cs(int level)
{
    gpio_set_level(OLED_CS_GPIO, level);
}

static void oled_reset(void)
{
    gpio_set_level(OLED_RST_GPIO, 0);
    vTaskDelay(pdMS_TO_TICKS(10));
    gpio_set_level(OLED_RST_GPIO, 1);
    vTaskDelay(pdMS_TO_TICKS(100));
}


static void send_cmd(uint8_t cmd)
{
    oled_set_dc(OLED_CMD_MODE);
    oled_set_cs(0);
    spi_transaction_t t = {
        .length = 8,            // 8 bits
        .tx_buffer = &cmd,
    };
    spi_device_polling_transmit(spi, &t); // 同步发送

    oled_set_cs(1);
}

static void send_data(const uint8_t *data, size_t len)
{
    if (len == 0) return;
    oled_set_dc(OLED_DATA_MODE);
    oled_set_cs(0);

    spi_transaction_t t = {
        .length = len * 8,      // 转为 bit 数
        .tx_buffer = data,
    };
    spi_device_polling_transmit(spi, &t);

    oled_set_cs(1);
}

// 初始化 SSD1309（兼容 SSD1306 指令集）
esp_err_t oled_init(void)
{
    // 配置 GPIO
    gpio_config_t io_conf = {0};
    io_conf.intr_type = GPIO_INTR_DISABLE;
    io_conf.mode = GPIO_MODE_OUTPUT;
    io_conf.pull_up_en = GPIO_PULLUP_DISABLE;
    io_conf.pull_down_en = GPIO_PULLDOWN_DISABLE;

    // CS, DC, RST
    io_conf.pin_bit_mask = (1ULL << OLED_CS_GPIO) |
                           (1ULL << OLED_DC_GPIO) |
                           (1ULL << OLED_RST_GPIO);
    gpio_config(&io_conf);

    // 初始化为默认状态
    gpio_set_level(OLED_CS_GPIO, 1);
    gpio_set_level(OLED_DC_GPIO, 0);
    gpio_set_level(OLED_RST_GPIO, 1);

    // 配置 SPI
    spi_bus_config_t buscfg = {
        .miso_io_num = -1,
        .mosi_io_num = OLED_MOSI_GPIO,
        .sclk_io_num = OLED_SCLK_GPIO,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = 128,
    };

    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 8 * 1000 * 1000, // 8MHz
        .mode = 0,
        .spics_io_num = -1, // 我们手动控制 CS
        .queue_size = 7,
        .flags = SPI_DEVICE_NO_DUMMY,
    };

    ESP_ERROR_CHECK(spi_bus_initialize(OLED_SPI_HOST, &buscfg, SPI_DMA_CH_AUTO));
    ESP_ERROR_CHECK(spi_bus_add_device(OLED_SPI_HOST, &devcfg, &spi));

    // 复位 OLED
    oled_reset();

    // 初始化指令序列（SSD1309 兼容 SSD1306）
    const uint8_t init_cmds[] = {
        0xAE, // Display OFF
        0xD5, 0x80, // Set display clock divide ratio/oscillator frequency
        0xA8, 0x3F, // Set multiplex ratio (1 to 64)
        0xD3, 0x00, // Set display offset
        0x40,       // Set start line
        0x8D, 0x14, // Charge pump setting (enable)
        0x20, 0x00, // Memory addressing mode: horizontal
        0xA1,       // Segment remap (flip horizontally)
        0xC8,       // COM output scan direction (flip vertically)
        0xDA, 0x12, // COM pins hardware configuration
        0x81, 0xCF, // Contrast control
        0xD9, 0xF1, // Pre-charge period
        0xDB, 0x40, // VCOMH deselect level
        0xA4,       // Entire display ON resume
        0xA6,       // Normal display
        0xAF,       // Display ON
    };

    for (int i = 0; i < sizeof(init_cmds); i++) {
        send_cmd(init_cmds[i]);
    }

    oled_clear();
    oled_display_on();
    ESP_LOGI(TAG, "OLED initialized");
    return ESP_OK;
}

void oled_clear(void)
{
    oled_fill_screen(0x00);
}

void oled_fill_screen(uint8_t data)
{
    for (int page = 0; page < 8; page++) {
        send_cmd(0xB0 + page); // Set page
        send_cmd(0x00);        // Lower column start address
        send_cmd(0x10);        // Upper column start address
        uint8_t buf[128];
        memset(buf, data, 128);
        send_data(buf, 128);
    }
}

void oled_set_pos(uint8_t page, uint8_t col)
{
    send_cmd(0xB0 + page);
    send_cmd(0x00 + (col & 0x0F));
    send_cmd(0x10 + ((col >> 4) & 0x0F));
}

void oled_write_data(const uint8_t *data, size_t len)
{
    send_data(data, len);
}

void oled_write_cmd(uint8_t cmd)
{
    send_cmd(cmd);
}

void oled_display_on(void)
{
    send_cmd(0xAF);
}

void oled_display_off(void)
{
    send_cmd(0xAE);
}

